int ft_find_next_prime(int nb) {

    int c = 2;
    int i  = 2;

    while (i <= nb)
    {
        while (c < nb - 1)
        {
            if (nb % c != 0 && c == nb)
            {
                return i;
            }
        }
        i++;
    }

    return (0);

}